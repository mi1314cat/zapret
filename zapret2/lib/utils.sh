#!/usr/bin/env bash
# ============================================================
# Zapret2 v7.0 - utils.sh（最终优化版）
# 提供：日志、颜色、root 检查、目录检查、命令执行包装
# ============================================================

set -euo pipefail

# ============================================================
# 颜色
# ============================================================
RED="\e[31m"
GREEN="\e[32m"
YELLOW="\e[33m"
BLUE="\e[34m"
CYAN="\e[36m"
RESET="\e[0m"

# ============================================================
# 日志函数（统一格式）
# ============================================================
log_info() {
    echo -e "${GREEN}[INFO]${RESET} $*"
}

log_warn() {
    echo -e "${YELLOW}[WARN]${RESET} $*"
}

log_error() {
    echo -e "${RED}[ERROR]${RESET} $*"
}

log_fatal() {
    echo -e "${RED}[FATAL]${RESET} $*"
    exit 1
}

# ============================================================
# root 权限检查
# ============================================================
require_root() {
    if [[ $EUID -ne 0 ]]; then
        log_fatal "必须以 root 身份运行"
    fi
}

# ============================================================
# 目录存在性检查
# ============================================================
ensure_dir() {
    local dir="$1"
    if [[ ! -d "$dir" ]]; then
        mkdir -p "$dir"
        log_info "创建目录：$dir"
    fi
}

# ============================================================
# 安全执行命令（带错误捕获）
# ============================================================
exec_cmd() {
    local cmd="$*"
    log_info "执行：$cmd"

    if ! eval "$cmd"; then
        log_error "命令失败：$cmd"
        return 1
    fi

    return 0
}

# ============================================================
# 轻量级文件锁（不与 zapret2d 的 flock 冲突）
# 用于：菜单、节点更新、策略更新等非核心流程
# ============================================================
with_lock() {
    local lockfile="$1"
    shift

    exec 8>"$lockfile"

    if ! flock -n 8; then
        log_warn "资源被占用：$lockfile"
        return 1
    fi

    "$@"
}

# ============================================================
# IP / 域名格式检查（白名单/黑名单可用）
# ============================================================
is_ip() {
    [[ "$1" =~ ^([0-9]{1,3}\.){3}[0-9]{1,3}$ ]]
}

is_domain() {
    [[ "$1" =~ ^[A-Za-z0-9.-]+$ ]]
}
